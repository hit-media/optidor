	<h2>Двери для любых целей и задач</h2>

	<p>Функционально и эстетически двери представляют собой один из самых важных элементов интерьера. Успешно
	   справляясь со своей главной функцией — разделением помещения на зоны в зависимости от их назначения —
	   двери
	   приобретают всё большую важность при проектировании жилища. Двери могут защитить от дыма и огня,
	   изолировать
	   источники шума, сохранить тепло в помещении.</p>

	<p>Разумеется, двери являются одним из ключевых элементов внешнего облика дома, квартиры или офиса. Входная
	   дверь определяет первое впечатление, которое остаётся от посещения того или иного жилища. Именно поэтому
	   дизайнеры постоянно ищут новые сочетания материалов и фурнитуры для дверей, чтобы решить даже самые
	   сложные
	   задачи.</p>
